# Different types of variables
# Store a number
num_1 = 69 # assigning 69 to num_1
print(num_1) # This displays what num_1 contains

# Store a string
name = "Ventresco"
print(name)
name_1 = "What the"
print(name)

# Do calculations in a variable and store the resut
sum = 5 + 5 # follows PEP* Python conventions
print(sum)
sum_string = "5 + 5 = 10" # this is a string
print(sum)


# Adding strings together is called concatenation
concatenation = name_1 + sum_string
print(concatenation)